package com.yk.entity;

import java.io.Serializable;
import java.util.Collection;
import javax.persistence.Basic;
import javax.persistence.CascadeType;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.FetchType;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.NamedQueries;
import javax.persistence.NamedQuery;
import javax.persistence.OneToMany;
import javax.persistence.Table;
import javax.validation.constraints.NotNull;
import javax.validation.constraints.Size;
import javax.xml.bind.annotation.XmlRootElement;

/**
 *
 * @author jhamil
 */
@Entity
@Table(name = "type_indicator")
@XmlRootElement
@NamedQueries({
    @NamedQuery(name = "TypeIndicator.findAll", query = "SELECT t FROM TypeIndicator t"),
    @NamedQuery(name = "TypeIndicator.findById", query = "SELECT t FROM TypeIndicator t WHERE t.id = :id"),
    @NamedQuery(name = "TypeIndicator.findByType", query = "SELECT t FROM TypeIndicator t WHERE t.type = :type")})
public class TypeIndicator implements Serializable {

    private static final long serialVersionUID = 1L;
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    @Basic(optional = false)
    @Column(name = "id")
    private Integer id;
    @Basic(optional = false)
    @NotNull
    @Size(min = 1, max = 45)
    @Column(name = "type")
    private String type;
    
    @OneToMany(cascade = CascadeType.ALL, mappedBy = "typeIndicatorId", fetch = FetchType.LAZY)
    private Collection<Device> deviceCollection;

    public TypeIndicator() {
    }

    public TypeIndicator(Integer id) {
        this.id = id;
    }

    public TypeIndicator(Integer id, String type) {
        this.id = id;
        this.type = type;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public Collection<Device> getDeviceCollection() {
        return deviceCollection;
    }

    public void setDeviceCollection(Collection<Device> deviceCollection) {
        this.deviceCollection = deviceCollection;
    }

    @Override
    public int hashCode() {
        int hash = 0;
        hash += (id != null ? id.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof TypeIndicator)) {
            return false;
        }
        TypeIndicator other = (TypeIndicator) object;
        if ((this.id == null && other.id != null) || (this.id != null && !this.id.equals(other.id))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "com.yk.TypeIndicator[ id=" + id + " ]";
    }
    
}
